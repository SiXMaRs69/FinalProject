package com.example.gamestu.model;

public class Game {
    private String game_type;
    private String game_price;
    private String game_detail;
    private String game_image;
    private String game_Dev;
    private String game_name;

    public String getGame_name() {
        return game_name;
    }

    public void setGame_name(String game_name) {
        this.game_name = game_name;
    }

    public String getGame_type() {
        return game_type;
    }

    public void setGame_type(String game_type) {
        this.game_type = game_type;
    }

    public String getGame_price() {
        return game_price;
    }

    public void setGame_price(String game_price) {
        this.game_price = game_price;
    }

    public String getGame_detail() {
        return game_detail;
    }

    public void setGame_detail(String game_detail) {
        this.game_detail = game_detail;
    }

    public String getGame_image() {
        return game_image;
    }

    public void setGame_image(String game_image) {
        this.game_image = game_image;
    }

    public String getGame_Dev() {
        return game_Dev;
    }

    public void setGame_Dev(String game_Dev) {
        this.game_Dev = game_Dev;
    }

}
